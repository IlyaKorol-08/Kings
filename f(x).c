#include <stdio.h>
#include <math.h>

double x(double x){ // f(x) = x
    return x;
}

double x_2(double x){ // f(x) = x^2
    return x*x;
}

double x_3(double x){ // f(x) = x^3
    return x*x*x;
}

double sinx(double x){ // f(x) = sin(x)
    return sin(x);
}

double cosx(double x){ // f(x) = cos(x)
    return cos(x);
}

double e_x(double x){ // f(x) = e^x
    return exp(x);
}